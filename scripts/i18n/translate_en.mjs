function setByPath(obj, path, value) {
  const parts = path.split('.')
  let cur = obj
  for (let i = 0; i < parts.length - 1; i++) {
    cur = cur[parts[i]]
    if (cur === undefined) return false
  }
  cur[parts[parts.length - 1]] = value
  return true
}

